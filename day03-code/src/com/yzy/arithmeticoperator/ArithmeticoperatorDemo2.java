package com.yzy.arithmeticoperator;

public class ArithmeticoperatorDemo2 {
    public static void main(String[] args) {
        //整数参与运算，结果只能为整数
        //小鼠参与运算，结果有可能不精确
        System.out.println(10 / 2);
        System.out.println(10 / 3);
        System.out.println(10 / 3.0);

        System.out.println(10 % 2);
        System.out.println(10 % 3);
    }
}
