package com.yzy.arithmeticoperator;

public class ArithmeticoperatorDemo3 {
    public static void main(String[] args) {
        byte b1 = 100;
        byte b2 = 100;
        int result = b1 + b2;
        byte result2 = (byte) (b1 + b2);
        System.out.println(result);
        System.out.println(result2);
    }
}
