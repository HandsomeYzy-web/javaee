package com.yzy.arithmeticoperator;

public class ArithmeticoperatorDemo5 {
    public static void main(String[] args) {
        // 自增自减运算符
        int a = 10;
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);
        a--;
        System.out.println(a);
        --a;
        System.out.println(a);
    }
}
